import bluetooth
import time

# Initialize BLE
ble = bluetooth.BLE()
ble.active(True)

if ble.active():
    print("BLE is active")
else:
    print("Failed to activate BLE")

def start_advertising(bt_data, interval):
    """Start advertising with the given data and interval."""
    print("Starting advertising...")
    ble.gap_advertise(interval, adv_data=bt_data)

def stop_advertising():
    """Stop the advertising."""
    print("Stopping advertising...")
    ble.gap_advertise(None)

def show_menu():
    """Display the available options."""
    print("Select an option to send:")
    for i, name in enumerate(payload_names, 1):
        print(f"{i}. {name}")

def main():
    """Main function to handle user input and start advertising."""
    show_menu()
    selected_option = int(input("Enter the option number you want: "))
    interval = int(input("Enter the advertising interval (e.g., 200): "))
    loop_interval = int(input("Enter the loop interval to reset the BLE adapter (seconds): "))
    
    if selected_option < 1 or selected_option > len(payloads):
        print("Invalid selection")
        return
    
    bt_data = payloads[selected_option - 1]
    
    print(f"Advertising started with payload {payload_names[selected_option - 1]}... Stop with Ctrl+C or reset the device")

    try:
        while True:
            start_advertising(bt_data, interval)
            time.sleep(loop_interval)
            stop_advertising()
            time.sleep(1)  # Pause before restarting the advertising
    except KeyboardInterrupt:
        print("Advertising stopped")
    except Exception as e:
        print(f"An error occurred: {e}")

